# float([x])
#  Retorna um float construído a partir de um número ou String x.
# Se x for uma String, ela deve conter um número decimal, e opcionalmente precedido porum sinal (+ ou -).
# Pode também ser uma infinidade positiva ou negativa, utilizando 'inf' ou 'Infinity'

print(float("Infinity"))
print(float(10))
print(float("-10"))
print(float("nan"))
print(float("1e-7"))
