# abs()
# Retorna o valor absoluto de um número inteiro ou flutuante.
# Se o argumento for um número complexo, sua magnitude é retornada.
# Se x definir __abs()__, abs(x) retorna x.__abs__()

A = 10
B = 10.5
C = 10.5555555555
D = -10.555555

print("Valores: ")
print(A,B,C,D)

print("abs(): ")
print(abs(A), abs(B), abs(C), abs(D))
